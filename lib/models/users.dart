class User {
  String 
  uid = "",
  username = "",
  fullname = "",
  phone = "", 
  grade = "";

  User(String uid, String username, String fullname, String phone, String grade) {
    this.uid = uid;
    this.username = username;
    this.fullname = fullname;
    this.phone = phone;
    this.grade = grade;
  }
}
